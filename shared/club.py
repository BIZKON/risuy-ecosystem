"""Клуб предпринимателей: тексты согласия (152-ФЗ ст.9 + ФЗ-38 — согласие на матчинг/
партнёрские предложения) и константы полей профиля.
⚠️ ШАБЛОН: финально выверяет юрист оператора."""

# Позиция в цепочке поставки: значение колонки club_profiles.chain_position → человекочитаемая подпись.
CLUB_CHAIN_POSITIONS: list[tuple[str, str]] = [
    ("before", "До меня в цепочке (даёт трафик)"),
    ("after", "После меня (даёт допродажу)"),
    ("both", "И до, и после"),
]

_CHAIN_POSITION_VALUES = {code for code, _ in CLUB_CHAIN_POSITIONS}


def _truthy(v) -> bool:
    return bool(str(v or "").strip())


def build_club_consent_text(kind: str, operator_name: str, operator_inn: str = "",
                            operator_email: str = "", privacy_hint: bool = False) -> str:
    """Текст согласия для consent_events.text_hash (кладём sha256 этого текста) — ОН ЖЕ
    показывается субъекту (показано==хэшировано, доказательная база 152-ФЗ ст.9). Приведён к
    составу эталона build_consent_text (оператор+ИНН, перечень ПДн, цель, действия, срок,
    локализация РФ, порядок отзыва). Рекламные «предложения о партнёрстве» ВЫНЕСЕНЫ из согласия
    на обработку (ФЗ-38 — отдельное opt-in согласие, см. offers_opt_in).
    kind: 'club_join' — вступление; 'network_join' — видимость в бирже (Уровень 2, вне Фазы 1);
    'intro' — обмен контактами при знакомстве. Неизвестный kind → пустая строка.
    ⚠️ ШАБЛОН: финальные формулировки выверяет юрист оператора."""
    inn_line = (f", ИНН {operator_inn}" if operator_inn else "")
    revoke = (f"командой /revoke или письмом на {operator_email}" if operator_email
              else "командой /revoke")
    if kind == "club_join":
        lines = [
            f"Оператор: {operator_name}{inn_line}.",
            "Вступая в Клуб предпринимателей, вы даёте согласие на обработку персональных данных вашего бизнеса:",
            "• Данные: название бизнеса, город, сфера/ОКВЭД, что предлагаете и что ищете, позиция в цепочке поставки.",
            "• Цель: подбор комплементарных партнёров внутри клуба и организация знакомств по взаимному согласию.",
            "• Действия: сбор, хранение, использование для автоматизированного подбора партнёров внутри клуба.",
            "• Срок: до отзыва согласия.",
            "• Хранение — на серверах в России.",
            f"• Отозвать согласие можно в любой момент: {revoke}.",
        ]
        if privacy_hint:
            lines.append("Полные условия — в политике обработки данных (кнопка ниже).")
        lines.append("Нажимая «Вступить в клуб», вы соглашаетесь на обработку указанных данных.")
        return "\n".join(lines)
    if kind == "network_join":
        return ("Дополнительно вы соглашаетесь сделать профиль вашего бизнеса видимым в общей бирже "
                "предпринимателей и получать предложения о партнёрстве от участников из других кабинетов.")
    if kind == "intro":
        lines = [
            f"Знакомство в Клубе предпринимателей. Оператор: {operator_name}{inn_line}.",
            "Принимая знакомство, вы соглашаетесь — при взаимном согласии обеих сторон — передать "
            "второму участнику: название бизнеса, город, сферу/ОКВЭД, имя контактного лица и телефон.",
            "Контактами обменяемся, только если согласитесь оба.",
            "• Цель: обсуждение возможного партнёрства.",
            f"• Отозвать согласие: {revoke}.",
        ]
        return "\n".join(lines)
    return ""


def validate_club_registration(d: dict) -> list[str]:
    """Проверка полей регистрации в клуб (чистая, без сети/БД). Возвращает список
    человекочитаемых ошибок (пусто = ок). Обязательные поля — название бизнеса, город,
    ОКВЭД; chain_position — необязателен, но если задан — должен быть одним из
    CLUB_CHAIN_POSITIONS."""
    errs: list[str] = []
    if not _truthy(d.get("display_name")):
        errs.append("Укажите название бизнеса.")
    if not _truthy(d.get("city")):
        errs.append("Укажите город.")
    if not _truthy(d.get("okved")):
        errs.append("Укажите ОКВЭД.")

    chain_position = d.get("chain_position")
    if chain_position is not None and chain_position not in _CHAIN_POSITION_VALUES:
        errs.append("Позиция в цепочке поставки указана некорректно.")

    return errs
