def club_intro_contact_card(contact: dict) -> str:
    """Plain-текст карточка контакта участника (без parse_mode). Поля берём из
    club_intro_reveal (display_name/city/okved + lead_name/lead_phone). Каждое поле —
    best-effort: если отсутствует, строку пропускаем (мягкая деградация, не падаем)."""
    lines: list[str] = []
    name = (contact.get("display_name") or "").strip()
    if name:
        lines.append(f"🏢 {name}")
    city = (contact.get("city") or "").strip()
    if city:
        lines.append(f"📍 {city}")
    okved = (contact.get("okved") or "").strip()
    if okved:
        lines.append(f"💼 {okved}")
    contact_name = (contact.get("lead_name") or "").strip()
    if contact_name:
        lines.append(f"👤 Контактное лицо: {contact_name}")
    phone = (contact.get("lead_phone") or "").strip()
    if phone:
        lines.append(f"📞 {phone}")
    return "\n".join(lines) if lines else "контакт временно недоступен"


